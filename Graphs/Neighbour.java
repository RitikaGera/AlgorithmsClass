
public class Neighbour {

	int label;
	int edgeweight;
	
	public Neighbour(int label, int edgeweight){
		this.label=label;
		this.edgeweight=edgeweight;
	}
	
	public int getLabel(){
		return label;
	}
	
	public int getEdgeweight() {
		return edgeweight;
	}
	public void setEdgeweight(int edgeweight) {
		this.edgeweight = edgeweight;
	}
	


}
